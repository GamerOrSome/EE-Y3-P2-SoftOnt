/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  char msg[] = "STM32 UART ready!\r\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);


  UB_VGA_Screen_Init(); // Init VGA-Screen

  int i;

  for(i = 0; i < LINE_BUFLEN; i++)
	  input.line_rx_buffer[i] = 0;

  // Reset some stuff
  input.byte_buffer_rx[0] = 0;
  input.char_counter = 0;
  input.command_execute_flag = FALSE;

  // HAl wants a memory location to store the charachter it receives from the UART
  // We will pass it an array, but we will not use it. We declare our own variable in the interupt handler
  // See stm32f4xx_it.c
  HAL_UART_Receive_IT(&huart2, input.byte_buffer_rx, BYTE_BUFLEN);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  API_clearscreen(VGA_COL_BLACK);
  HAL_Delay(3000);
  for(int i = 10; i < 240; i++)
  {
    API_draw_line(1, i, 319, i, i, 1, 0);
  }
  API_draw_bitmap(130, 90, 1);

  HAL_Delay(1000);

  API_clearscreen(VGA_COL_WHITE);
 
  HAL_Delay(100);

  while (1)
  {

    // Check if we have a complete line to process
    if (input.command_execute_flag == TRUE) {
      // Echo back the received line
      HAL_UART_Transmit(&huart2, (uint8_t*)input.line_rx_buffer, input.char_counter, HAL_MAX_DELAY);
      HAL_UART_Transmit(&huart2, (uint8_t*)"\r\n", 2, HAL_MAX_DELAY);
      
      struct LogicInterface cmd = {0};
      memset(&cmd, 0, sizeof(cmd));

      uint32_t kommando = 0;
      uint32_t strnglgth = 0;

      for (int j = 0; j < input.msglen+1; j++)
      {
    	if(input.line_rx_buffer[j] == '\0')
    	{
    		strnglgth = j;
    	}
      }
      for(int j = 0; j < input.msglen; j++)
      {
    	if(input.line_rx_buffer[j] == ',')
    	{
    		kommando = j-1;
    		for(int i = 0; i <= kommando; i++)
    		{
    			cmd.function_name[i] = input.line_rx_buffer[i];
    		}

    		break;
    	}
      }

      cmd.argument_len = strnglgth - kommando;
      char arg[128]= {0};
      for(int j = 0; j<= input.msglen-kommando;j++)
      {
    	  arg[j] = input.line_rx_buffer[j+kommando+2];
      }

      cmd.arguments = &arg;
      execute_command(&cmd);
      
      // Reset for next command
      input.char_counter = 0;
      input.command_execute_flag = FALSE;
      memset((void*)input.line_rx_buffer, 0, LINE_BUFLEN);
    }

    HAL_Delay(100);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

