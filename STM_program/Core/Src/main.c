/**
  * @brief  Reads a string from UART and sends it back
  * @param  huart: UART handle
  * @param  buffer: Pointer to buffer to store received string
  * @param  maxLen: Maximum length of string to receive
  * @retval Number of characters received
  */
uint16_t UART_ReadStringEcho(UART_HandleTypeDef *huart, char *buffer, uint16_t maxLen)
{
  uint16_t index = 0;
  uint8_t rxChar;
  
  while (index < maxLen - 1) {
    // Read one character from UART
    if (HAL_UART_Receive(huart, &rxChar, 1, HAL_MAX_DELAY) == HAL_OK) {
      buffer[index] = rxChar;
      
      // Echo back the character
      HAL_UART_Transmit(huart, &rxChar, 1, HAL_MAX_DELAY);
      
      // Check for string terminator (newline or carriage return)
      if (rxChar == '\n' || rxChar == '\r') {
        buffer[index] = '\0';  // Null-terminate the string
        return index;
      }
      index++;
    } else {
      break;  // Error occurred
    }
  }
  
  buffer[index] = '\0';  // Null-terminate the string
  return index;
}



